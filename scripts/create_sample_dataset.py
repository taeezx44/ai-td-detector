#!/usr/bin/env python3
"""
Create Sample Research Dataset for AI-TD Detector

Generates a realistic simulated dataset that meets research criteria
for testing the statistical analysis pipeline.

Usage:
    python scripts/create_sample_dataset.py --output data/sample_research_dataset.csv
"""

import argparse
import json
import random
from datetime import datetime, timedelta
from pathlib import Path

import pandas as pd
import numpy as np


class SampleDatasetGenerator:
    """Generate realistic sample dataset for AI-TD research."""
    
    def __init__(self):
        self.languages = ['Python', 'JavaScript', 'TypeScript', 'Jupyter Notebook']
        self.domains = ['web-development', 'data-science', 'mobile-apps', 'devops-tools']
        self.ai_confidence_levels = ['High', 'Medium', 'Low', 'None']
        
        # Repository name patterns
        self.ai_repo_patterns = [
            'ai-{tool}', '{project}-ai-assisted', 'copilot-{project}', 
            'llm-{domain}', 'generated-{app}', 'smart-{system}'
        ]
        
        self.human_repo_patterns = [
            '{project}', '{tool}-lib', '{domain}-framework', 
            '{app}-server', '{system}-core', 'lib{project}'
        ]
    
    def generate_repository(self, repo_id: int, is_ai: bool) -> dict:
        """Generate a single repository record."""
        
        # Select language and domain
        language = random.choice(self.languages)
        domain = random.choice(self.domains)
        
        # Generate repository name
        if is_ai:
            name_pattern = random.choice(self.ai_repo_patterns)
            ai_confidence = random.choice(['High', 'Medium', 'Medium'])  # Bias toward higher confidence
        else:
            name_pattern = random.choice(self.human_repo_patterns)
            ai_confidence = 'None'
        
        # Fill name pattern
        project_names = ['analytics', 'dashboard', 'api', 'parser', 'utils', 'framework', 'client', 'server']
        tools = ['detector', 'analyzer', 'processor', 'generator', 'converter', 'validator']
        
        name = name_pattern.format(
            project=random.choice(project_names),
            tool=random.choice(tools),
            domain=domain.replace('-', ''),
            app=random.choice(['app', 'service', 'platform', 'system']),
            system=random.choice(['core', 'engine', 'kernel', 'runtime'])
        )
        
        # Generate owner
        owners = ['techcorp', 'datalab', 'devteam', 'researchgroup', 'opensource', 'startup']
        owner = random.choice(owners)
        full_name = f"{owner}/{name}"
        
        # Generate realistic metadata
        size_kb = random.randint(5000, 50000)  # Within research criteria
        stars = random.randint(1, 1000)  # At least 1 star
        forks = random.randint(0, stars // 2)
        
        # Generate dates (1-3 years old)
        days_ago = random.randint(365, 1095)
        created_at = datetime.now() - timedelta(days=days_ago)
        updated_at = created_at + timedelta(days=random.randint(30, days_ago))
        
        # Generate description
        if is_ai:
            descriptions = [
                f"AI-powered {language} {domain} toolkit",
                f"Machine learning {language} library for {domain}",
                f"Intelligent {language} framework with AI assistance",
                f"Automated {language} {domain} solution"
            ]
        else:
            descriptions = [
                f"{language} {domain} library and toolkit",
                f"Comprehensive {language} framework for {domain}",
                f"Lightweight {language} {domain} utilities",
                f"Production-ready {language} {domain} solution"
            ]
        
        description = random.choice(descriptions)
        
        # Generate AI commit data (only for AI repos)
        ai_commit_ratio = 0.0
        ai_commits_found = 0
        ai_commit_date = ""
        ai_commit_message = ""
        ai_marker_count = 0
        ai_signals = []
        
        if is_ai:
            ai_commit_ratio = random.uniform(0.05, 0.30)  # 5-30% AI commits
            total_commits = random.randint(50, 500)
            ai_commits_found = int(total_commits * ai_commit_ratio)
            
            # Generate sample AI commit
            ai_commit_date = (updated_at - timedelta(days=random.randint(1, 30))).isoformat() + 'Z'
            
            commit_messages = [
                f"add {random.choice(['feature', 'function', 'module'])} - generated by copilot",
                f"implement {random.choice(['parser', 'analyzer', 'utils'])} - ai-assisted",
                f"update {random.choice(['docs', 'tests', 'examples'])} - co-authored-by: github copilot",
                f"fix {random.choice(['bug', 'issue', 'error'])} - generated by chatgpt"
            ]
            
            ai_commit_message = random.choice(commit_messages)
            ai_marker_count = 1
            ai_signals = [r"generated\s+by\s+(copilot|chatgpt|gpt-?4|claude|ai|llm)"]
        
        return {
            'name': full_name,
            'url': f"https://github.com/{full_name}",
            'description': description,
            'language': language,
            'stars': stars,
            'forks': forks,
            'created_at': created_at.isoformat() + 'Z',
            'updated_at': updated_at.isoformat() + 'Z',
            'size_kb': size_kb,
            'is_private': False,
            'default_branch': 'main',
            
            # AI-specific data
            'ai_commit_sha': f"{random.randint(1000000, 9999999):x}" if is_ai else "",
            'ai_commit_message': ai_commit_message,
            'ai_commit_date': ai_commit_date,
            'ai_marker_count': ai_marker_count,
            'ai_confidence': ai_confidence,
            'ai_signals': json.dumps(ai_signals),
            
            # Analysis results
            'total_commits_analyzed': random.randint(50, 500),
            'ai_commits_found': ai_commits_found,
            'ai_commit_ratio': round(ai_commit_ratio, 4),
            'ai_confidence_level': ai_confidence,
            'ai_commits': json.dumps([]),  # Would contain actual commit data
            'likely_ai_assisted': is_ai,
            
            # Collection metadata
            'collected_at': datetime.now().isoformat(),
            'search_query': 'sample_dataset',
            'domain': domain,
            'repo_id': repo_id
        }
    
    def create_balanced_dataset(self, total_repos: int = 40, ai_ratio: float = 0.5) -> pd.DataFrame:
        """
        Create a balanced dataset with AI and Human repositories.
        
        Args:
            total_repos: Total number of repositories
            ai_ratio: Ratio of AI repositories (0.0-1.0)
            
        Returns:
            DataFrame with balanced dataset
        """
        print(f"Creating balanced dataset: {total_repos} repos, {ai_ratio*100:.0f}% AI")
        
        ai_count = int(total_repos * ai_ratio)
        human_count = total_repos - ai_count
        
        repositories = []
        
        # Generate AI repositories
        print(f"Generating {ai_count} AI repositories...")
        for i in range(ai_count):
            repo = self.generate_repository(i, is_ai=True)
            repositories.append(repo)
        
        # Generate Human repositories
        print(f"Generating {human_count} Human repositories...")
        for i in range(ai_count, total_repos):
            repo = self.generate_repository(i, is_ai=False)
            repositories.append(repo)
        
        # Create DataFrame
        df = pd.DataFrame(repositories)
        
        # Shuffle the dataset
        df = df.sample(frac=1, random_state=42).reset_index(drop=True)
        
        print(f"Created dataset with {len(df)} repositories")
        return df
    
    def add_realistic_ai_td_scores(self, df: pd.DataFrame) -> pd.DataFrame:
        """
        Add realistic AI-TD scores based on repository characteristics.
        
        Args:
            df: Input DataFrame
            
        Returns:
            DataFrame with AI-TD scores
        """
        print("Adding realistic AI-TD scores...")
        
        scores = []
        
        for _, repo in df.iterrows():
            is_ai = repo['ai_confidence'] != 'None'
            
            if is_ai:
                # AI repositories tend to have higher scores
                base_score = random.uniform(0.4, 0.8)
                
                # Adjust based on confidence
                if repo['ai_confidence'] == 'High':
                    base_score += random.uniform(0.1, 0.2)
                elif repo['ai_confidence'] == 'Medium':
                    base_score += random.uniform(0.0, 0.1)
                
                # AI-specific patterns
                complexity_score = random.uniform(0.2, 0.6)
                duplication_score = random.uniform(0.3, 0.8)  # Higher duplication
                documentation_score = random.uniform(0.5, 0.9)  # Poor documentation
                error_handling_score = random.uniform(0.6, 0.9)  # Poor error handling
                
            else:
                # Human repositories tend to have lower scores
                base_score = random.uniform(0.1, 0.4)
                
                complexity_score = random.uniform(0.1, 0.4)
                duplication_score = random.uniform(0.0, 0.3)  # Lower duplication
                documentation_score = random.uniform(0.0, 0.4)  # Better documentation
                error_handling_score = random.uniform(0.2, 0.6)  # Better error handling
            
            # Calculate final AI-TD score using research formula
            ai_td_score = (
                0.30 * complexity_score +
                0.25 * duplication_score +
                0.20 * documentation_score +
                0.25 * (1 - error_handling_score)
            )
            
            scores.append({
                'ai_td_score': round(ai_td_score, 4),
                'complexity_score': round(complexity_score, 4),
                'duplication_score': round(duplication_score, 4),
                'documentation_score': round(documentation_score, 4),
                'error_handling_score': round(error_handling_score, 4),
                'severity': 'HIGH' if ai_td_score >= 0.6 else 'MEDIUM' if ai_td_score >= 0.3 else 'LOW'
            })
        
        # Add scores to DataFrame
        scores_df = pd.DataFrame(scores)
        df = pd.concat([df, scores_df], axis=1)
        
        print(f"Added AI-TD scores (range: {df['ai_td_score'].min():.3f} - {df['ai_td_score'].max():.3f})")
        
        return df
    
    def save_dataset(self, df: pd.DataFrame, output_path: str):
        """Save dataset to CSV file."""
        df.to_csv(output_path, index=False)
        print(f"Dataset saved to {output_path}")
    
    def create_analysis_plan(self, df: pd.DataFrame, output_path: str):
        """Create analysis plan for the dataset."""
        plan = {
            'dataset_info': {
                'total_repos': len(df),
                'ai_repos': len(df[df['ai_confidence'] != 'None']),
                'human_repos': len(df[df['ai_confidence'] == 'None']),
                'languages': df['language'].unique().tolist(),
                'domains': df['domain'].unique().tolist(),
                'ai_confidence_distribution': df['ai_confidence'].value_counts().to_dict()
            },
            'score_statistics': {
                'overall_mean': df['ai_td_score'].mean(),
                'overall_median': df['ai_td_score'].median(),
                'ai_mean': df[df['ai_confidence'] != 'None']['ai_td_score'].mean(),
                'human_mean': df[df['ai_confidence'] == 'None']['ai_td_score'].mean(),
                'expected_effect_size': abs(
                    df[df['ai_confidence'] != 'None']['ai_td_score'].mean() - 
                    df[df['ai_confidence'] == 'None']['ai_td_score'].mean()
                )
            },
            'research_readiness': {
                'sample_size_adequate': len(df) >= 34,
                'power_analysis': {
                    'effect_size': abs(
                        df[df['ai_confidence'] != 'None']['ai_td_score'].mean() - 
                        df[df['ai_confidence'] == 'None']['ai_td_score'].mean()
                    ),
                    'sample_size': len(df),
                    'power_adequate': len(df) >= 34  # Simplified
                },
                'balance_adequate': abs(len(df[df['ai_confidence'] != 'None']) - len(df[df['ai_confidence'] == 'None'])) <= 5
            }
        }
        
        with open(output_path, 'w') as f:
            json.dump(plan, f, indent=2)
        
        print(f"Analysis plan saved to {output_path}")
        return plan


def main():
    """Main entry point."""
    parser = argparse.ArgumentParser(description="Create Sample Research Dataset")
    parser.add_argument("--output", default="data/sample_research_dataset.csv", help="Output CSV file")
    parser.add_argument("--size", type=int, default=40, help="Dataset size")
    parser.add_argument("--ai-ratio", type=float, default=0.5, help="AI repository ratio")
    parser.add_argument("--plan", help="Create analysis plan file")
    
    args = parser.parse_args()
    
    generator = SampleDatasetGenerator()
    
    # Create dataset
    df = generator.create_balanced_dataset(args.size, args.ai_ratio)
    
    # Add realistic AI-TD scores
    df = generator.add_realistic_ai_td_scores(df)
    
    # Save dataset
    generator.save_dataset(df, args.output)
    
    # Create analysis plan
    if args.plan:
        generator.create_analysis_plan(df, args.plan)
    
    # Print summary
    print(f"\nDataset Summary:")
    print(f"  Total repositories: {len(df)}")
    print(f"  AI repositories: {len(df[df['ai_confidence'] != 'None'])}")
    print(f"  Human repositories: {len(df[df['ai_confidence'] == 'None'])}")
    print(f"  AI-TD Score range: {df['ai_td_score'].min():.3f} - {df['ai_td_score'].max():.3f}")
    print(f"  Mean AI-TD Score: {df['ai_td_score'].mean():.3f}")
    
    ai_mean = df[df['ai_confidence'] != 'None']['ai_td_score'].mean()
    human_mean = df[df['ai_confidence'] == 'None']['ai_td_score'].mean()
    print(f"  AI mean score: {ai_mean:.3f}")
    print(f"  Human mean score: {human_mean:.3f}")
    print(f"  Expected effect size: {abs(ai_mean - human_mean):.3f}")


if __name__ == "__main__":
    main()
